-module(erlping_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    httpc:set_options([{socket_opts, [{cb_info, {erlping_tcp, tcp, tcp_closed, tcp_error}}]}]),
    erlping_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

